local pedindex = {}

local localPlayer = GetPlayerPed(-1)
local isHoldingHandsUp = false

--Lock all AI vehicles
Citizen.CreateThread(function()
    while true do
		Citizen.Wait(0)       
		if DoesEntityExist(GetVehiclePedIsTryingToEnter(PlayerPedId(localPlayer))) then
			local veh = GetVehiclePedIsTryingToEnter(PlayerPedId(localPlayer))
			local lock = GetVehicleDoorLockStatus(veh)

			if lock == 7 then
				SetVehicleDoorsLocked(veh, 2)
			end

			local pedd = GetPedInVehicleSeat(veh, -1)

			if pedd then
				SetPedCanBeDraggedOut(pedd, false)
			end
		end
    end
end)

